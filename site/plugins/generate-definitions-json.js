/**
 * @file generate-definitions-json.js
 * @description After-pass Cook plugin. Reads site/src/_data/vocabulary.json
 * and emits three derived assets into dist/pages/:
 *
 *   /definitions.json         flat object keyed by concept @id with
 *                             { term, definition, href } per concept
 *
 *   /js/definition-popover.js progressive enhancement: feature-detects the
 *                             Popover API, fetches /definitions.json on first
 *                             interaction (caching in sessionStorage under
 *                             `vb:definitions`), wires click on
 *                             a[data-concept]. Modifier-clicks fall through.
 *
 *   /css/definition-popover.css minimal cue + popover chrome
 *
 * Loaded by site/src/layouts/docs.html so every doc page can resolve
 * inline `<a data-concept>` references without each page opting in.
 *
 * Spec: admin/r-n-d/evaluate/glossary-system.md
 * Decision: admin/r-n-d/evaluate/decisions.md (adopted 2026-04-27).
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const VOCAB_PATH = resolve(__dirname, '../src/_data/vocabulary.json');

function definitionsFromVocabulary(vocab) {
  const out = {};
  for (const c of vocab.concepts || []) {
    if (!c['@id']) continue;
    out[c['@id']] = {
      term: c['skos:prefLabel'] || c['@id'],
      definition: c['skos:definition'] || '',
      href: `/glossary/#term-${c['@id']}`
    };
  }
  return out;
}

const POPOVER_SCRIPT = `/**
 * definition-popover.js
 *
 * Progressive enhancement for inline glossary terms (a[data-concept]).
 * Feature-detects the Popover API; on first interaction, fetches
 * /definitions.json and caches it in sessionStorage under 'vb:definitions'.
 * Modifier-clicks (Cmd/Ctrl/Shift/Alt/middle-click) fall through to normal
 * navigation so deep-linking, "open in new tab", and accessibility tools
 * keep working.
 *
 * Generated by site/plugins/generate-definitions-json.js. Do not edit by hand.
 */
(function () {
  if (!('popover' in HTMLElement.prototype)) return;

  var links = document.querySelectorAll('a[data-concept]');
  if (!links.length) return;

  var popover = null;
  var termEl, defEl, linkEl;
  var definitions = null;
  var loadingPromise = null;

  function ensurePopover() {
    if (popover) return;
    popover = document.createElement('div');
    popover.id = 'dfn-popover';
    popover.setAttribute('popover', '');
    popover.innerHTML =
      '<p data-dfn-term></p>' +
      '<p data-dfn-definition></p>' +
      '<a data-dfn-link>View in glossary &rarr;</a>';
    document.body.appendChild(popover);
    termEl = popover.querySelector('[data-dfn-term]');
    defEl  = popover.querySelector('[data-dfn-definition]');
    linkEl = popover.querySelector('[data-dfn-link]');
  }

  function loadDefinitions() {
    if (definitions) return Promise.resolve(definitions);
    if (loadingPromise) return loadingPromise;
    try {
      var cached = sessionStorage.getItem('vb:definitions');
      if (cached) {
        definitions = JSON.parse(cached);
        return Promise.resolve(definitions);
      }
    } catch (_) { /* storage unavailable */ }
    loadingPromise = fetch('/definitions.json')
      .then(function (r) { return r.ok ? r.json() : null; })
      .then(function (data) {
        if (!data) return null;
        definitions = data;
        try { sessionStorage.setItem('vb:definitions', JSON.stringify(data)); } catch (_) {}
        return data;
      })
      .catch(function () { return null; });
    return loadingPromise;
  }

  function position(link) {
    var rect = link.getBoundingClientRect();
    var popRect = popover.getBoundingClientRect();
    var top  = rect.bottom + 8;
    var left = rect.left;
    if (top + popRect.height > window.innerHeight) {
      top = rect.top - popRect.height - 8;
    }
    if (left + popRect.width > window.innerWidth) {
      left = window.innerWidth - popRect.width - 16;
    }
    if (left < 8) left = 8;
    popover.style.position = 'fixed';
    popover.style.top  = top + 'px';
    popover.style.left = left + 'px';
  }

  function show(link, entry) {
    ensurePopover();
    termEl.textContent = entry.term;
    defEl.textContent  = entry.definition;
    linkEl.href        = entry.href;
    try { popover.showPopover(); } catch (_) { return; }
    position(link);
  }

  links.forEach(function (link) {
    link.addEventListener('click', function (e) {
      if (e.ctrlKey || e.metaKey || e.shiftKey || e.altKey || e.button !== 0) return;
      var concept = link.dataset.concept;
      if (!concept) return;
      e.preventDefault();
      loadDefinitions().then(function (defs) {
        if (!defs) { window.location.href = link.href; return; }
        var entry = defs[concept];
        if (!entry) { window.location.href = link.href; return; }
        show(link, entry);
      });
    });
  });
})();
`;

const POPOVER_CSS = `/**
 * definition-popover.css
 *
 * Generated by site/plugins/generate-definitions-json.js. Do not edit by hand.
 *
 * Targets [data-concept] / native popover with data-attribute selectors so
 * the cue and chrome never depend on class names or theme overrides.
 */

dfn > a[data-concept],
a[data-concept] {
  text-decoration-style: dotted;
  text-underline-offset: 0.15em;
}

#dfn-popover {
  max-width: 32ch;
  padding: 0.75rem 1rem;
  border: 1px solid light-dark(#ccc, #555);
  border-radius: 0.375rem;
  background: light-dark(#fff, #1a1a1a);
  color: light-dark(#111, #eee);
  box-shadow: 0 4px 12px rgb(0 0 0 / 0.15);
  margin: 0;
}

#dfn-popover [data-dfn-term] {
  font-weight: 700;
  margin: 0;
}

#dfn-popover [data-dfn-definition] {
  margin-block: 0.375rem 0.5rem;
}

#dfn-popover [data-dfn-link] {
  font-size: 0.85em;
}
`;

export class GenerateDefinitionsJson {
  async init() {
    const cwd = process.cwd();
    const distDir = resolve(cwd, 'dist/pages');
    if (!existsSync(distDir)) return;

    const raw = await readFile(VOCAB_PATH, 'utf-8');
    const vocab = JSON.parse(raw);
    const definitions = definitionsFromVocabulary(vocab);

    const defsPath = resolve(distDir, 'definitions.json');
    await writeFile(defsPath, JSON.stringify(definitions, null, 2) + '\n', 'utf-8');
    console.log(`  ✓ Generated definitions.json (${Object.keys(definitions).length} terms)`);

    const jsDir  = resolve(distDir, 'js');
    const cssDir = resolve(distDir, 'css');
    await mkdir(jsDir,  { recursive: true });
    await mkdir(cssDir, { recursive: true });
    await writeFile(resolve(jsDir,  'definition-popover.js'),  POPOVER_SCRIPT, 'utf-8');
    await writeFile(resolve(cssDir, 'definition-popover.css'), POPOVER_CSS,    'utf-8');
    console.log('  ✓ Generated definition-popover.{js,css}');
  }
}
