#!/usr/bin/env node
/**
 * Generate extended emoji dataset from GitHub's gemoji database.
 *
 * Fetches gemoji's emoji.json and produces src/data/emoji-data-extended.js
 * containing ~1700 additional emoji not in the core VB set.
 *
 * Format: compact arrays [shortcode, emoji, name, group] for minimal size
 * while still supporting picker search (name) and category tabs (group).
 *
 * Usage: node scripts/generate-emoji-extended.js
 */

import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

const GEMOJI_URL = 'https://raw.githubusercontent.com/github/gemoji/master/db/emoji.json';

/** Map gemoji categories to VB group names */
const CATEGORY_MAP = {
  'Smileys & Emotion': 'smileys',
  'People & Body': 'people',
  'Animals & Nature': 'animals',
  'Food & Drink': 'food',
  'Travel & Places': 'travel',
  'Activities': 'activities',
  'Objects': 'objects',
  'Symbols': 'symbols',
  'Flags': 'flags',
};

async function main() {
  // 1. Extract existing VB shortcodes to avoid duplicates
  const coreSrc = readFileSync(join(ROOT, 'src/data/emoji-data.js'), 'utf8');
  const coreShortcodes = new Set();
  const re = /shortcode: '([^']+)'/g;
  let m;
  while ((m = re.exec(coreSrc)) !== null) coreShortcodes.add(m[1]);

  // Also extract aliases so we don't duplicate those either
  const aliasRe = /\['([^']+)',\s*'([^']+)'\]/g;
  while ((m = aliasRe.exec(coreSrc)) !== null) coreShortcodes.add(m[1]);

  console.log(`Core VB shortcodes: ${coreShortcodes.size}`);

  // 2. Fetch gemoji data
  console.log(`Fetching gemoji from ${GEMOJI_URL}...`);
  const resp = await fetch(GEMOJI_URL);
  if (!resp.ok) throw new Error(`Fetch failed: ${resp.status}`);
  const gemoji = await resp.json();
  console.log(`Gemoji entries: ${gemoji.length}`);

  // 3. Build extended entries (skip those in core)
  const extended = [];
  for (const entry of gemoji) {
    if (!entry.aliases || entry.aliases.length === 0) continue;
    const group = CATEGORY_MAP[entry.category] || 'symbols';

    for (const alias of entry.aliases) {
      if (coreShortcodes.has(alias)) continue;
      extended.push([alias, entry.emoji, entry.description, group]);
    }
  }

  console.log(`Extended entries (excluding core): ${extended.length}`);

  // 4. Generate the output file
  const lines = extended.map(e =>
    `  ${JSON.stringify(e)},`
  ).join('\n');

  const output = `/**
 * emoji-data-extended: Full gemoji-compatible emoji dataset
 *
 * Auto-generated by scripts/generate-emoji-extended.js
 * Source: github/gemoji (${gemoji.length} entries, ${extended.length} after excluding VB core)
 *
 * Format: [shortcode, emoji, name, group]
 * Compact array format for minimal file size.
 *
 * This file is a standalone module — load it via <script> tag to supplement
 * the core emoji set. Load order does not matter.
 *
 * @example
 * <script type="module" src="/cdn/emoji-extended.js"><\/script>
 */

const EXTENDED = [
${lines}
];

// Register with VB core if already loaded, otherwise store for pickup
if (globalThis.__vbEmojiRegister) {
  globalThis.__vbEmojiRegister(EXTENDED);
} else {
  globalThis.__vbEmojiExtended = EXTENDED;
}
`;

  const outPath = join(ROOT, 'src/data/emoji-data-extended.js');
  writeFileSync(outPath, output, 'utf8');

  const sizeKB = (Buffer.byteLength(output) / 1024).toFixed(1);
  console.log(`\nWrote ${outPath}`);
  console.log(`Size: ${sizeKB} KB (${extended.length} entries)`);
}

main().catch(e => {
  console.error('Failed:', e);
  process.exit(1);
});
